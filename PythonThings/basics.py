#Control Flow

#for
for i in range(0, 10):
    print(i, end=' ')

print('\n')

#while
i = 0
while (i < 10):
    print(i, end=' ')
    i += 1

print('\n')
#if else

a = 20
b = 10

if(a == b):
    print('a == b')
elif(a <= b):
    print('a <= b')
elif(a >= b):
    print('a >= b')
else:
    print('a else b')

